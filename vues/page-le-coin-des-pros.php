  <h1 class="h1-page-title">LE COIN DES PROFESSIONNELS</h1>
  <br>
  <div class="p-haut-de-page">
    Via diff&eacute;rentes <u>solutions et conditions facilitatrices</u>, 
    je collabore avec les corps de m&eacute;tiers suivants, pour aider leurs clients 
    &agrave; <u>concr&eacute;tiser leurs projets</u> : 
    <b>notaire</b>, avocat, <b>conseiller bancaire</b>, agent immobilier, <b>artisan</b>, 
    vendeur (meubles de cuisine, chambre...), conseiller en gestion de patrimoine.
    <br><br>
  </div>
  <div class="p-haut-de-page box p-3">
    Rappel des principales conditions que je peux proposer : <br>
    <ul class="ul-fleches">
      <li><u>Age en fin de pr&ecirc;t</u> jusqu'&agrave; <b>95 ans</b>,</li>
      <li><u>Dur&eacute;e du pr&ecirc;t</u> jusqu'&agrave; <b>15 ans</b> en conso / <b>35 ans</b> en immobilier,</li>
      <li><u>Taux d'endettement</u> maximum jusqu'&agrave; <b>45 %</b>.</li>
    </ul>

